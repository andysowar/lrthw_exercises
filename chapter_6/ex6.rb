# A variable making 10 equal to types_of_people.
types_of_people = 10
# This variable is set to a string with a variable inside it.
x = "There are #{types_of_people} types of people."
# Another variable set to a string.
binary = 'binary'
# Another variable set to a string.
do_not = 'don\'t'
# Another variable set to a string with 2 variables inside it.
y = "Those who know #{binary} and those who #{do_not}."

# This puts will output what x is equal to.
puts x
# This puts will output what y is equal to.
puts y

# This will put the string along with the output of the variable.
puts "I said: #{x}."
# This will put the string along with the output of the variable.
puts "I also said: '#{y}'."

# Setting the variable hilarious to 'false'.
hilarious = false
# Setting variable joke_evaluation to a string along with variable 'hilarious'.
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# puts the output of variable 'joke_evaluation'.
puts joke_evaluation

# Setting variable w to a string.
w = 'This is the left side of...'
# Setting variable e to a string.
e = 'a string with a right side.'

# The output of these two variables will be put together.
# In this case two strings will get attached.
puts w + e
